export const translations: Record<Lang, Dict> = { en, hi };

/** Look up a key for a language, interpolating {placeholders} from params. */
export function translate(lang: Lang, id: string, params?: Record<string, string | number>): string {
  const value = translations[lang]?.[id] ?? translations.en[id] ?? id;
  if (!params) return value;
  return value.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`));
}
