/**
 * E2E TEST CASE — "Promoter completes Company Setup and uploads documents"
 *
 * Fills the Company Setup form programmatically (same API the form submits to)
 * with the real public disclosures of Shivalic Power Control Ltd (NSE Emerge
 * SME IPO, July 2024), generates matching test PDFs, pushes them through the
 * upload → classification → fact-extraction pipeline, runs the rule engine,
 * and asserts every step.
 *
 * Usage:
 *   1. Start the app:  npm run dev   (or npm run build && npm start)
 *   2. Run:            node scripts/seed-test-company.mjs [--no-docs]
 *   BASE_URL=http://localhost:3000 by default (override via env).
 *
 * Figures sourced from public IPO trackers (Chittorgarh/Bigul/Screener),
 * rounded — test fixture only, not audited values.
 */

