// ── tiny assertion helpers ──────────────────────────────────────────────────
let failures = 0;
const ok = (cond, label, detail = "") => {
  console.log(`  ${cond ? "✅" : "❌"} ${label}${detail ? ` — ${detail}` : ""}`);
  if (!cond) failures++;
};
const post = async (url, body) => {
  const res = await fetch(BASE + url, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(body),
  });
  return { status: res.status, data: await res.json().catch(() => ({})) };
};

// ── test-fixture PDFs consistent with the profile ───────────────────────────
async function makeFixturePdfs(dir) {
  const { default: PDFDocument } = await import("pdfkit");
  fs.mkdirSync(dir, { recursive: true });
  const write = (name, title, lines) =>
    new Promise((resolve) => {
      const doc = new PDFDocument({ margin: 50 });
      const stream = fs.createWriteStream(path.join(dir, name));
      doc.pipe(stream);
      doc.fontSize(14).text(title, { underline: true });
      doc.moveDown();
      doc.fontSize(10);
      for (const line of lines) { doc.text(line); doc.moveDown(0.4); }
      doc.moveDown();
      doc.fontSize(8).fillColor("#666")
        .text("TEST FIXTURE generated by scripts/seed-test-company.mjs from public IPO-tracker figures. Not an audited document.");
      doc.end();
      stream.on("finish", resolve);
    });

  await write("Shivalic Audited Financials FY2024.pdf",
    "SHIVALIC POWER CONTROL LIMITED — Audited Financial Statements FY2024", [
      "CIN: U31200HR2004PLC035502",
      "Registered office: Faridabad, Haryana",
      "Statement of Profit and Loss for the year ended 31 March 2024",
      "Revenue from operations: Rs. 102 crore",
      "EBITDA: Rs. 19 crore",
      "Profit after tax: Rs. 10.9 crore",
      "",
      "Balance Sheet as at 31 March 2024",
      "Net worth: Rs. 42 crore",
      "Total borrowings: Rs. 32 crore",
      "Trade receivables: Rs. 34.7 crore",
      "",
      "Cash flow from operations: Rs. -9 crore",
    ]);
  await write("Shivalic GST Summary FY2024.pdf",
    "GSTR-9 Annual Return Summary — FY2024", [
      "Legal name: Shivalic Power Control Limited",
      "GSTIN: 06AAJCS4970M1ZI",
      "Aggregate taxable turnover: Rs. 100.8 crore",
      "Note: Demand notice of Rs. 15 lakh for FY2023 pending; reply filed.",
    ]);
  await write("Shivalic Litigation Declaration.pdf",
    "Declaration on Litigation — Shivalic Power Control Limited", [
      "We declare that there is no pending litigation, NIL, against the Company,",
      "its promoter or directors, other than as separately disclosed.",
      "Signed: Amit Kanwar Jindal, Promoter & Managing Director",
    ]);
  return [
    "Shivalic Audited Financials FY2024.pdf",
    "Shivalic GST Summary FY2024.pdf",
    "Shivalic Litigation Declaration.pdf",
  ].map((f) => path.join(dir, f));
}

// ── the test case ───────────────────────────────────────────────────────────
async function main() {
  console.log(`\nE2E: Company Setup auto-fill → upload → analysis   (server: ${BASE})\n`);

  // Step 0: server reachable
  const up = await fetch(BASE + "/api/companies").catch(() => null);
  if (!up) {
    console.error(`❌ Server not reachable at ${BASE}. Start it first: npm run dev`);
    process.exit(1);
  }

  // Step 1: submit the Company Setup form
  console.log("Step 1 — create company (fills every Company Setup field):");
  const created = await post("/api/companies", COMPANY);
  ok(created.status === 200 && created.data.company?.id, "company created", created.data.company?.name);
  ok(created.data.company?.cin === COMPANY.cin, "CIN saved");
  ok(created.data.company?.financials?.length === 3, "3-year financial snapshot saved");

  // Step 2: run the rule engine on profile data alone
  console.log("Step 2 — run deterministic rule engine:");
  const a1 = await post("/api/analysis", {});
  const s1 = a1.data.analysis?.scores;
  ok(a1.status === 200 && typeof s1?.overall === "number", "readiness score computed", `${s1?.overall}/100 — ${s1?.statusLine}`);
  const gaps1 = a1.data.analysis?.gaps ?? [];
  ok(gaps1.length > 0, "gaps detected from profile", `${gaps1.length} gaps`);
  const checks = a1.data.analysis?.checks ?? [];
  ok(checks.some((c) => /customer concentration/i.test(c.ruleName) && c.status === "fail"),
    "customer-concentration rule fired (top-3 = 45%)");
  ok(checks.some((c) => /receivable spike/i.test(c.ruleName) && c.status === "fail"),
    "receivable-spike rule fired (FY2024 receivables +161% vs revenue +23%)");
  ok(checks.some((c) => /cash flow quality/i.test(c.ruleName) && c.status !== "pass"),
    "cash-flow-quality rule fired (CFO -₹9 Cr vs PAT ₹10.9 Cr)");

  // Step 3: upload test-fixture documents through the real pipeline
  if (WITH_DOCS) {
    console.log("Step 3 — generate & upload fixture PDFs (AI classification + fact extraction; may take ~1 min):");
    const dir = path.join(os.tmpdir(), "ipo-saathi-fixtures");
    const files = await makeFixturePdfs(dir);
    const fd = new FormData();
    for (const f of files) fd.append("files", new Blob([fs.readFileSync(f)], { type: "application/pdf" }), path.basename(f));
    const res = await fetch(BASE + "/api/documents/upload", { method: "POST", body: fd });
    const up2 = await res.json();
    ok(res.status === 200 && up2.documents?.length === 3, "3 documents uploaded & classified",
      up2.documents?.map((d) => `${d.fileName.split(" ").slice(-2).join(" ")}→${d.category}`).join(", "));
    const factsRes = await (await fetch(BASE + "/api/facts")).json();
    ok((factsRes.facts?.length ?? 0) > 0, "facts extracted with provenance", `${factsRes.facts?.length} facts`);
    const s2 = up2.analysis?.scores;
    ok(typeof s2?.overall === "number", "analysis re-ran after upload", `score ${s2?.overall}/100, RPT ${s2?.rptScore}/100`);
    const finChecks = up2.analysis?.financialChecks ?? [];
    ok(finChecks.some((f) => /GST/i.test(f.checkName)),
      "GST-vs-books cross-check ran (audited ₹102 Cr vs GST ₹100.8 Cr)");
  } else {
    console.log("Step 3 — skipped (--no-docs)");
  }

  console.log(`\n${failures === 0 ? "✅ ALL CHECKS PASSED" : `❌ ${failures} check(s) failed`}`);
  console.log("Open the app — Dashboard, Readiness, Coverage Matrix, Gap Report and Evidence are now populated.");
  console.log("Next: review facts in Extraction & Evidence, build Objects of Issue, then click Generate Draft.\n");
  process.exit(failures === 0 ? 0 : 1);
}

main().catch((e) => { console.error("❌ Test crashed:", e.message); process.exit(1); });
